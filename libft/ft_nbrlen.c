#include "libft.h"

size_t	ft_nbrlen(int n)
{
	size_t	nlen;

	nlen = 0;
	if (n == -2147483648)
		return (11);
	else if (n < 0)
	{
		nlen++;
		n = -n;
	}
	while (n > 0)
	{
		n = n / 10;
		nlen++;
	}
	return (nlen);
}